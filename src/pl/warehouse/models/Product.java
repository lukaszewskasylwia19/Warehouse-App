package pl.warehouse.models;

/**
 * Reprezentuje produkt znajdujacy sie w magazynie
 */
public class Product {
    /**
     * Id produktu
     */
    public Integer Id;

    /**
     * Nazwa produktu
     */
    public String Name;

    /**
     * Ilosc produktu
     */
    public Integer Quantity;

    /**
     * Cena produktu
     */
    public Integer Price;

    /**
     * Kategoria produktu
     */
    public String Category;

    /**
     * Szczegoly produktu
     */
    public String Details;
}
